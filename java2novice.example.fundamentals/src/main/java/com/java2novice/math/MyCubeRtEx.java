package com.java2novice.math;

/**
 * Below example shows how to get cube root of(立方根) the given number in java by using Math.cbrt() method. - See more at:
 * http://java2novice.com/java-fundamentals/math/cube-root/#sthash.yjD4gSXg.dpuf
 * @author java2novice
 * @date 2015年12月9日下午4:48:46
 */
public class MyCubeRtEx {
	public static void main(String a[]) {
		System.out.println("Cube root of 10: " + Math.cbrt(10));
		System.out.println("Cube root of 625: " + Math.cbrt(625));
		System.out.println("Cube root of 1090: " + Math.cbrt(1090));
	}
}