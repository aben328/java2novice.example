package com.myjava.string;

/**
 * Below example shows how to convert the case of a given string. toUpperCase() method converts all string characters to
 * upper case. toLowerCase() method converts all string characters to lower case. - See more at:
 * http://java2novice.com/java_string_examples/case_change/#sthash.WBybaMeF.dpuf
 * @author java2novice
 */
public class MyStringCase {
	public static void main(String a[]) {
		String str = "Change My Case";
		System.out.println("Upper Case: " + str.toUpperCase());
		System.out.println("Lower Case: " + str.toLowerCase());
	}
}